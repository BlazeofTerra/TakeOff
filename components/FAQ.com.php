		    <h3 class="panel-title">5. Annoying pop-up adverts</h3>
		  </div>
		  <div class="panel-body">
		    <p class="question">
		    	<b>Q.</b>  If I take up a business profile or make a post for a specific aircraft, product or service, will viewers and prospective customers be subject to the irritating and intrusive pop-up advertisements which are a feature of so many websites?
		    </p>

		    <br>

		    <p class="answer">
		    	<b>A.</b>   The answer is a definite ‘NO’. ‘Take-off Aviation Marketplace’ is a platform dedicated to our advertisers in our defined formats and we will not accept adverts of the ‘pop-up’ type.
		    </p>
		  </div>
		</div>

