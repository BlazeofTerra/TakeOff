		  <div class="panel-heading">
		    <h3 class="panel-title">8. Category displays</h3>
		  </div>
		  <div class="panel-body">
		    <p class="question">
		    	<b>Q.</b> Why do some categories display ‘Post cards’ and others ‘Business cards’ when the category is selected?
		    </p>

		    <br>

		    <p class="answer">
		    	<b>A.</b>  The categories ‘Aircraft’ and ‘Merchandise’ display ‘Post cards’ because viewers are more likely to be searching for specific aircraft or products. (For these categories, ‘Business cards’ can be viewed by selecting the appropriate ‘Business sub-category’ in the menu box on the lower right hand side of the page.) On the other hand, all the other categories display the ‘business cards’ of the advertisers as it is more likely that viewers who select those categories are interested in the businesses which can offer the various types of service. (For these categories, ‘Post cards’ can be viewed by selecting the appropriate ‘Sub-category’ in the menu box on the lower right hand side of the page.)
		    </p>
