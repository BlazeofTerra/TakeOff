		    <h3 class="panel-title">1. I have my own website</h3>
		  </div>
		  <div class="panel-body">
		    <p class="question">
		    	<b>Q.</b> Our business has its own website. Why should we post on Take-off Aviation Marketplace?	
		    </p>

		    <br>

		    <p class="answer">
		    	<b>A.</b> We emphasise that we definitely do not see Take-off Aviation Marketplace as an alternative to advertisers’ own websites. Rather, it will complement their websites by providing an initial point of reference and link to the businesses’ websites for browsers and potential customers who otherwise would not necessarily identify sellers and service providers who may be of interest to them.
		    </p>
		  </div>
		</div>

		<!--*************************************************************************************-->
		<div class="panel panel-primary">
		  <div class="panel-heading">
		    <h3 class="panel-title">2. Why must viewers ‘click here’ to view our contact details?</h3>
		  </div>
		  <div class="panel-body">
		    <p class="question">
		    	<b>Q.</b> To view our contact numbers and email and website addresses on our business profile and posts, it is necessary to ‘click here’. Why are these details not displayed so as to be easily viewed?
		    </p>

		    <br>

		    <p class="answer">
		    	<b>A.</b>  This is to protect our advertisers from Internet ‘bots’ which could result in your contact details being subjected to large quantities of unsolicited emails and messages. (A ‘bot’ is a software application that runs automated tasks over the internet.)
		    </p>
		  </div>
		</div>

		<!--*************************************************************************************-->
		<div class="panel panel-primary">
		  <div class="panel-heading">
		    <h3 class="panel-title">3. Post pages, post cards, business profiles and business cards.</h3>
		  </div>
		  <div class="panel-body">
		    <p class="question">
		    	<b>Q.</b>  What are the respective functions of:
							<ul>
								<li>Post Pages</li>
								<li>Post Cards</li>
								<li>Business profiles</li>
								<li>Business cards</li>
							</ul>
			</p>

		    <br>

		    <p class="answer">
		    	<b>A.</b>  
		    	These are described as follows:<br><br>

					<b>Post pages</b> provide for a full description, including photographs if desired, of any aircraft, product or service which an advertiser wishes to sell. Post pages are available for both individual and business advertisers.<br><br>

					The prime function of <b>Post cards</b> is to provide a quick form of reference for users searching for particular items or services. They detail in brief summary form specific aircraft, products and services offered.<br><br>

					<b>Business profiles</b> are reserved for businesses who register and pay for them. They incorporate the following features:<br><br>
					<ul>
						<li>There is ample provision for listing full details of the business, its contact details and contact personnel.</li>
						<li>They are valid for a full year from date of posting.</li>
						<li>They may be edited and updated, free of charge, by the authorized business representative, as often as is required during the year.</li>
						<li>Logos and up to 6 photographs may be included.</li>
						<li>The summarized version of a Business profile is referred to as a ‘Business card’ and is displayed when a category (other than ‘Aircraft’ or ‘Merchandise’ – see 8 below) is selected. The full Business profile is accessed by clicking on the logo in the ‘Business card’.</li>
						<li>The intention is that businesses view their Business profiles as attractive and informative showcases for a wide range of viewers and prospective customers.</li>
					</ul>	
		    </p>
		  </div>
		</div>


		<!--*************************************************************************************-->
		<div class="panel panel-primary">
		  <div class="panel-heading">
		    <h3 class="panel-title">4. Categories, Business sub-categories and Sub-categories.</h3>
		  </div>
		  <div class="panel-body">
		    <p class="question">
		    	<b>Q.</b> The following terms are used:
		    	<ul>
		    		<li>Category</li>
		    		<li>Business sub-category</li>
		    		<li>Sub-categories</li>
		    	</ul>					
				What are the differences and usage of each?
		    </p>

		    <br>

		    <p class="answer">
		    	<b>A.</b>  These may be explained as follows:<br><br>
				The <b>category</b> defines the main area(s) of operation or activity of a business. The various categories under which businesses may choose to register are shown in the boxes underneath the ‘Take-off Aviation Marketplace’ logo on the home page. Businesses may register under more than one category (up to five) at no extra cost.<br><br>
				A <b>business sub-category</b> indicates a particular activity (or activities) under each category. The purpose of sub-categories is to enable potential customers / clients to navigate easily to a product or service they require. A business may indicate more than one sub-category (up to five in total) at no extra cost. (The available Business sub-categories are displayed on the right of each category page.)<br><br>
				<b>Sub-categories</b> relate to posts relating to specific aircraft, products and services advertised in post pages and post cards (see 3 above). (The available Sub-categories are displayed on the right of each category page.)

		    </p>
		  </div>
		</div>

