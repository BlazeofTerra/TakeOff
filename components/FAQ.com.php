		  <div class="panel-heading">
		    <h3 class="panel-title">6. Display of businesses</h3>
		  </div>
		  <div class="panel-body">
		    <p class="question">
		    	<b>Q.</b> When a viewer clicks on a particular category, how many businesses are displayed on the first page? What determines which ones are displayed?
		    </p>

		    <br>

		    <p class="answer">
		    	<b>A.</b>  Each page has provision for up to 30 ‘business cards’ which provide logos, contact details and links (click on the logo) to the full business profile. Should there be more than 30 businesses registered under a particular category, there will be clear ‘continuation’ links to the subsequent page. ‘Business cards’ will be displayed in the order of most recent activity, which may be:<br><br>
				registration of a new business profile; or<br><br>
				making of a new post for a specific aircraft, product or service by a registered business.
		    </p>
		  </div>
		</div>

		<!--*************************************************************************************-->
		<div class="panel panel-primary">
		  <div class="panel-heading">
		    <h3 class="panel-title">7. Display of post cards</h3>
		  </div>
		  <div class="panel-body">
		    <p class="question">
		    	<b>Q.</b> When a viewer clicks on a sub-category for a specific type of aircraft, product or service, if there are a number of post cards, what is the order in which these will be displayed?
		    </p>

		    <br>

		    <p class="answer">
		    	<b>A.</b>  These will be displayed in the order in which the posts were made or renewed.
		    </p>
